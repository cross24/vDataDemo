function drawChats(type, oIds) {
    switch (type) {
        case "chart-table":
        case "chart-text":
        case "chart-line":
            drawline(oIds);
            break;
        case "chart-area":
            drawArea(oIds);
            break;
        case "chart-column":
            drawColumn(oIds);    
            break;
        case "chart-bar":
            drawBar(oIds);    
            break;
        case "chart-pie":
            drawPie(oIds);
            break;
        case "chart-rose-pie":
            drawRosePie(oIds);
            break;
        case "chart-scatter":
            drawScatter(oIds);
            break;
        case "chart-bubble":
            drawBubble(oIds);
            break;
        case "chart-radar":
            drawRadar(oIds);
            break;
        case "chart-gauge":
            drawGauge(oIds);
            break;
        case "chart-doughnut":
            drawDoughnut(oIds);
            break;
        case "chart-nest-pie":
            drawNestPie(oIds);
            break;
        case "chart-area-map":
            drawAreaMap(oIds);
            break;
        case "chart-funnel":
            drawFunnel(oIds);
            break;
    }
}
